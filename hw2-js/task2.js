// 2. Повторить с switch, при условии
// num = 5 - "Tiny"
// num = 10 - "Small"
// num = 15 - "Medium"
// num = 20 - "Large"
// num = 25 - "Huge"

let num = prompt("tell me size?");

switch (num) {
  case "5":
    alert("Tiny");
    break;
  case "10":
    alert("Small");
    break;
  case "15":
    alert("Medium");
    break;
  case "20":
    alert("Large");
    break;
  case "25":
    alert("Huge");
    break;
}
